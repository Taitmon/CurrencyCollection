package com.company;


import java.math.BigDecimal;

public class Main
{
    public static void main(String[] args)
    {
        System.out.println("Welcome to the Ultimate Currency Collection Program!");
        System.out.println("First we will create a default currency collection with some exciting coins not worth any more than their face value.");

        final int quarterCount = 10;
        final int dimeCount = 5;
        final int nickelCount = 3;
        final int pennyCount = 2;


        CurrencyCollection currencyCollection = new CurrencyCollection(quarterCount, dimeCount, nickelCount, pennyCount);

        System.out.println("Let's take a look at the value of your new currency collection.");
        System.out.println();

        System.out.println("Your collection has total face value of " + currencyCollection.getFaceValue());
        System.out.println("Your collection has total collectible value of " + currencyCollection.getCollectibleValue());

        System.out.println();
        System.out.println("Let's add some more valuable coins to your collection.");

        final int pennyYear1 = 1900;
        final int nickelYear1 = 1903;
        final int dimeYear1 = 1905;
        final int quarterYear1 = 1908;

        currencyCollection.addPenny(pennyYear1);
        currencyCollection.addNickel(nickelYear1);
        currencyCollection.addDime(dimeYear1);
        currencyCollection.addQuarter(quarterYear1);

        final int pennyYear2 = 1940;
        final int nickelYear2 = 1993;
        final int dimeYear2 = 1978;
        final int quarterYear2 = 2008;

        currencyCollection.addPenny(pennyYear2);
        currencyCollection.addNickel(nickelYear2);
        currencyCollection.addDime(dimeYear2);
        currencyCollection.addQuarter(quarterYear2);

        System.out.println("Let's take a look at the value of your growing currency collection.");
        System.out.println();
        System.out.println("Your collection has a total face value of " + currencyCollection.getFaceValue());
        System.out.println("Coins have total collectible value of " + currencyCollection.getCollectibleValue());

        System.out.println();
        System.out.println("Let's take a look at the value of your collection in more detail.");
        System.out.println();
        System.out.println("Quarters have total face value of " + currencyCollection.getQuartersFaceValue());
        System.out.println("Quarters have total collectible value of " + currencyCollection.getQuartersCollectibleValue());

        System.out.println("Dimes have total face value of " + currencyCollection.getDimesFaceValue());
        System.out.println("Dimes have total collectible value of " + currencyCollection.getDimesCollectibleValue());

        System.out.println("Nickels have total face value of " + currencyCollection.getNickelsFaceValue());
        System.out.println("Nickels have total collectible value of " + currencyCollection.getNickelsCollectibleValue());

        System.out.println("Pennies have total face value of " + currencyCollection.getPenniesFaceValue());
        System.out.println("Pennies have total collectible value of " + currencyCollection.getPenniesCollectibleValue());

    }
}

